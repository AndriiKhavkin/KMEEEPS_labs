def priority_to_score(priority: str) -> int:
    """
    Переводить текстовий пріоритет у числову шкалу для сортування та графіка.
    """
    scores = {
        "Низький": 1,
        "Середній": 2,
        "Високий": 3,
        "Критичний": 4,
    }
    return scores.get(priority, 0)


def main() -> None:
    decision_rows = []

    for obj in objects:
        level, priority = define_general_level(obj["value"])
        recommendation = make_recommendation(obj["subsystem"], obj["value"])

        decision_rows.append({
            "Об'єкт": obj["name"],
            "Підсистема": obj["subsystem"],
            "Показник": obj["indicator"],
            "Значення": obj["value"],
            "Одиниця / тип": obj["unit"],
            "Рівень стану": level,
            "Пріоритет": priority,
            "Рекомендоване управлінське рішення": recommendation,
            "PriorityScore": priority_to_score(priority),
        })

    df = pd.DataFrame(decision_rows)

    # Сортування за пріоритетом і значенням показника
    df = df.sort_values(
        by=["PriorityScore", "Значення"],
        ascending=[False, False]
    )

    # Збереження CSV для звіту / подальшої роботи
    csv_filename = "management_decisions.csv"
    df.drop(columns=["PriorityScore"]).to_csv(
        csv_filename,
        index=False,
        encoding="utf-8-sig"
    )

    # Вивід у консоль
    print("\nПІДСИСТЕМА ПРИЙНЯТТЯ УПРАВЛІНСЬКИХ РІШЕНЬ")
    print("=" * 80)

    for _, row in df.iterrows():
        object_name = row["Об'єкт"]
        print(f"\nОб'єкт: {object_name}")
        print(f"Підсистема: {row['Підсистема']}")
        print(f"Показник: {row['Показник']} = {row['Значення']}")
        print(f"Рівень стану: {row['Рівень стану']}")
        print(f"Пріоритет: {row['Пріоритет']}")
        print(f"Рішення: {row['Рекомендоване управлінське рішення']}")

    print("\n" + "=" * 80)
    print(f"CSV-файл збережено: {csv_filename}")

    # Побудова графіка пріоритетності
    chart_df = df.copy()
    chart_df["Коротка назва"] = chart_df["Об'єкт"].apply(
        lambda x: x[:35] + "..." if len(x) > 35 else x
    )

    plt.figure(figsize=(12, 7))
    plt.barh(chart_df["Коротка назва"], chart_df["Значення"])
    plt.xlabel("Значення інтегрального / нормованого показника")
    plt.ylabel("Об'єкт моніторингу")
    plt.title("Пріоритетність управлінського реагування за інтегральними показниками")
    plt.gca().invert_yaxis()
    plt.tight_layout()

    chart_filename = "risk_chart.png"
    plt.savefig(chart_filename, dpi=300)
    plt.close()

    print(f"Графік збережено: {chart_filename}")


if __name__ == "__main__":
    main()