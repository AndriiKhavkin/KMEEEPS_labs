# Кольори маркерів для різних типів об'єктів
color_map = {
    "air": "green",
    "rad": "black",
    "water": "blue",
    "energy": "red",
    "econ": "orange",
    "health": "cadetblue",
    "waste": "gray"
}

# Додавання маркерів на карту
for obj in objects:
    popup_content = f"""
    <div style="width: 280px">
        <h4>{obj['name']}</h4>
        {obj['info']}
    </div>
    """

    folium.Marker(
        location=obj["loc"],
        popup=folium.Popup(popup_content, max_width=330),
        tooltip=obj["name"],
        icon=folium.Icon(
            color=color_map.get(obj["type"], "blue"),
            icon="info-sign"
        )
    ).add_to(m)

